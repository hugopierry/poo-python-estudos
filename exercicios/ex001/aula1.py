# Classe
"""
É o molde (modelo) que define como um objeto será criado.

Ela especifica:
- Atributos: características que os objetos terão.
    Exemplo: peso, cor, altura, nome, idade.

- Métodos: ações que os objetos poderão executar.
    Exemplo:
        cozinhar()
        congelar()
        andar()
        comer()

A classe ainda não é um objeto.
Ela apenas define como os objetos serão.
"""

# Instanciar
"""
Instanciar significa criar um objeto a partir de uma classe.

Exemplo:

class Pessoa:
    pass

p1 = Pessoa()

Nesse caso, p1 é uma instância da classe Pessoa.
"""

# Objeto
"""
Objeto é uma instância de uma classe.

É a representação concreta do modelo criado pela classe.

Todo objeto possui:
- atributos (características)
- métodos (comportamentos)
- estado atual (valores atuais dos atributos)

Exemplo:

class Carro:
    pass

meu_carro = Carro()

'meu_carro' é um objeto criado a partir da classe Carro.
"""

